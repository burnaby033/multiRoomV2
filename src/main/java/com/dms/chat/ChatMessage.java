package com.dms.chat;

public class ChatMessage {
    String chatRoomId;
    String message;
    String type;
    String writer;   
    int chatId;
    int size;
	public String getChatRoomId() {
		return chatRoomId;
	}
	public String getMessage() {
		return message;
	}
	public String getType() {
		return type;
	}
	public String getWriter() {
		return writer;
	}
	public int getChatId() {
		return chatId;
	}
	public int getSize() {
		return size;
	}
    
	
      
}
